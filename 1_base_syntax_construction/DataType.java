class DataType {
  public static void main(String[] args) {

    // byte (1 byte), short (2 byte), int (4 byte), long (8 byte)
    // boolean (1 byte), char (2 byte), float (4 byte), double (8 byte) 


    byte int_a = Byte.MAX_VALUE;
    short int_b = Short.MAX_VALUE;
    int int_c = Integer.MAX_VALUE;
    long long_d = Long.MAX_VALUE;
    char char_e = 'c';
    float float_f = Float.MAX_VALUE;
    double double_g = Double.MAX_VALUE;

    System.out.println(int_a);
    System.out.println(int_b);
    System.out.println(int_c);
    System.out.println(long_d);
    System.out.println(char_e);
    System.out.println(float_f);
    System.out.println(double_g);
  }
}
