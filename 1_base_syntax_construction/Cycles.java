class Cycles {
  public static void main(String[] args) {
    int i = 0;
    int b = 0;
    int l = 0; 
    // for, while, do, while
    for (; i < 32; i++) {
      System.out.print(i);
    }

    while(b < 32) {
      System.out.println(b);
      b++;
    }

    do {
      System.out.println(l);
      l++;
    }while(l < 32);
    
    
  }
}
